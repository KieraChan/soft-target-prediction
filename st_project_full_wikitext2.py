# One full pass over the grouped WikiText-2 train split.
FULL_EPOCH_STEPS = len(train_loader)
FULL_MAX_TRAIN_STEPS = FULL_EPOCH_STEPS
FULL_EVAL_EVERY = 500
CURVE_EVAL_MAX_BATCHES = 50
